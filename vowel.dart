void main() {
  // Assign an alphabet
  String alphabet = 'a';

  // Check if the alphabet is a vowel or consonant
  if ((alphabet == 'a' ||
      alphabet == 'e' ||
      alphabet == 'i' ||
      alphabet == 'o' ||
      alphabet == 'u')) {
    print("$alphabet is a vowel.");
  } else {
    print("$alphabet is a consonant.");
  }
}
